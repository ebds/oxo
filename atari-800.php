            <h3 class="oxo__headline--third">Ataris Einstieg in die Welt der Heimcomputer</h3>
            <p class="oxo__paragraph">
                Befragt man
                <a class="oxo__link" href="http://de.wikipedia.org/wiki/Nolan_Bushnell" target="_blank">
                    Nolan Bushnell
                </a>
                , den legendären Atari-Gründer, was
                sein größter Fehler als Geschäftsmann war, erzählt er die
                Geschichte von dem abgelehnten Angebot, den späteren
                Apple-Computer zu kaufen und als Atari-Computer zu produzieren.
            </p>
            <p class="oxo__paragraph">
                Diesen Fehler versuchte man bei Atari 1979 mit den beiden
                kompatiblen Einsteiger-Computern 400 und 800 wiedergutzumachen.
                Technisch waren sie fast baugleich.
                Allerdings war der 800er mit einem 48-KB-Arbeitsspeicher
                (anstelle der 16 KB des 400ers), einem zweiten
                Erweiterungsschacht und einer ordentlichen Tastatur ausgerüstet.
            </p>
            <p class="oxo__paragraph">
                Beide Modelle waren die ersten Heimcomputer, die mit extra für
                sie entworfenen sog. "Customchips", u.a. für die Graphikausgabe,
                ausgestattet wurden und so den Hauptprozessor entlasteten.
                Damit bot der 800er eine schnellere und höher auflösende
                Graphik als die seinerzeit marktgängigen Spielekonsolen.
                Erwähnenswert sind außerdem die vier (!) Joystickports, in die
                die Controller des populären Atari VCS passten.
            </p>
            <p class="oxo__paragraph">
                Designer des Computers war übrigens Jay Miner, der später auch
                für die Konzeption des Amiga verantwortlich zeichnete.
                Einige Besonderheiten der beiden kleinen Ataris sollten in
                leicht abgewandelter Form später auch dem Amiga zu seinem
                Ruhm verhelfen.
            </p>
